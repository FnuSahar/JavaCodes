package interviewQuestion;

public class PrintCasesInArray {

	public static void main(String[] args) {
		
		String[][] testStatus= {{"tc001","passed"},
				               {"tc002","failed"},
				               {"tc003","passed"}};
		
		for (int i = 0; i < testStatus.length; i++) {
			for (int j = 0; j < testStatus[i].length; j++) {
				
			
			System.out.print(testStatus[i][j]);}
			System.out.println();
			
		}
		
		
		
		
		}
		
		
		

	

}
